import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const consoleRoot = resolve(here, '..')
const repoRoot = resolve(consoleRoot, '..', '..')

function shortSha() {
  if (process.env.CARACAL_DEV_SHA) return process.env.CARACAL_DEV_SHA
  try {
    return execSync('git rev-parse --short HEAD', { cwd: repoRoot }).toString().trim()
  } catch {
    return 'nogit'
  }
}

function baseVersion() {
  const raw = readFileSync(resolve(repoRoot, 'packages/engine/runtime/release.json'), 'utf8')
  return JSON.parse(raw).version
}

const ciRelease = process.env.CARACAL_RELEASE_VERSION
const sha = shortSha()
const version = ciRelease ?? `${baseVersion()}-dev.sha${sha}`
const mode = ciRelease ? (ciRelease.includes('-rc.') ? 'rc' : 'stable') : 'dev'

if (ciRelease && /(^|[+-])dev\./.test(ciRelease)) {
  process.stderr.write(`stampRelease: refusing dev-suffixed CARACAL_RELEASE_VERSION '${ciRelease}'\n`)
  process.exit(1)
}

const body =
  `// Copyright (C) 2026 Garudex Labs.  All Rights Reserved.
// Caracal, a product of Garudex Labs
//
// Stamped Console identity. Generated by scripts/stampDev.mjs in dev or scripts/stampRelease.mjs in CI.

export const CARACAL_CONSOLE_VERSION = '${version}'
export const CARACAL_CONSOLE_MODE: 'dev' | 'rc' | 'stable' = '${mode}'
export const CARACAL_CONSOLE_SHA = '${sha}'
`

writeFileSync(resolve(consoleRoot, 'src/version.gen.ts'), body, 'utf8')
process.stdout.write(`stamped ${mode} Console version ${version}\n`)
